const bannerImages = [
    "../img/banner/shop banner2.png",
    "../img/banner/shop banner3.avif",
    "../img/banner/shop banner 4.jpg",
];

let currentBannerIndex = 0;
const shopAds = document.getElementById('shop-ads');

function slideImage() {
    // Move current image left off-screen
    shopAds.style.transform = "translateX(-100%)";
    shopAds.style.opacity = "0";

    setTimeout(() => {
        // Change image and reset position to right
        shopAds.src = bannerImages[currentBannerIndex];
        shopAds.style.transition = "none";
        shopAds.style.transform = "translateX(100%)";

        // Force reflow to apply the position
        void shopAds.offsetWidth;

        // Animate image back into view from right to center
        shopAds.style.transition = "transform 0.5s ease-in-out, opacity 0.5s";
        shopAds.style.transform = "translateX(0)";
        shopAds.style.opacity = "1";

        // Update index
        currentBannerIndex = (currentBannerIndex + 1) % bannerImages.length;
    }, 500);
}

setInterval(slideImage, 4000);




